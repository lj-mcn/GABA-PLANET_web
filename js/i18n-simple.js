// 简化的国际化类
class SimpleI18n {
    constructor() {
        this.currentLanguage = localStorage.getItem('language') || 'zh';
        this.init();
    }
    
    init() {
        console.log('🌐 简化版国际化系统初始化，当前语言:', this.currentLanguage);
        this.bindEvents();
        this.updateLanguage();
    }
    
    bindEvents() {
        const langSwitcher = document.getElementById('langSwitcher');
        if (langSwitcher) {
            langSwitcher.addEventListener('click', () => {
                this.switchLanguage();
            });
            console.log('✅ 语言切换按钮绑定成功');
        } else {
            console.warn('⚠️ 未找到语言切换按钮');
        }
    }
    
    switchLanguage() {
        this.currentLanguage = this.currentLanguage === 'zh' ? 'en' : 'zh';
        localStorage.setItem('language', this.currentLanguage);
        console.log('🔄 切换语言到:', this.currentLanguage);
        this.updateLanguage();
    }
    
    updateLanguage() {
        console.log('🔄 开始更新语言显示');
        
        // 更新页面标题
        const titleKey = document.documentElement.getAttribute('data-title-key') || 'home.title';
        document.title = this.t(titleKey);
        
        // 更新所有翻译元素
        this.updateElements();
        
        // 更新语言内容区块
        this.updateLanguageContent();
        
        // 更新语言切换按钮
        this.updateLanguageSwitcher();
        
        // 更新轮换文本
        this.updateRotatingText();
        
        // 更新品牌图片
        this.updateBrandImage();
        
        // 更新邮件链接
        this.updateContactEmail();
        
        console.log('✅ 语言更新完成');
    }
    
    updateElements() {
        const elements = document.querySelectorAll('[data-i18n]');
        console.log(`🔍 找到 ${elements.length} 个需要翻译的元素`);
        
        elements.forEach((element, index) => {
            const key = element.getAttribute('data-i18n');
            const translation = this.t(key);
            
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translation;
            } else {
                element.textContent = translation;
            }
            
            console.log(`📝 ${index + 1}. "${key}" → "${translation}"`);
        });
    }
    
    updateLanguageContent() {
        // 处理多语言内容区块
        const langContents = document.querySelectorAll('.lang-content');
        if (langContents.length > 0) {
            langContents.forEach(element => {
                const elementLang = element.getAttribute('data-lang');
                if (elementLang === this.currentLanguage) {
                    element.classList.add('active');
                } else {
                    element.classList.remove('active');
                }
            });
            console.log(`🌍 已更新 ${langContents.length} 个语言内容区块到: ${this.currentLanguage}`);
        }
    }
    
    updateLanguageSwitcher() {
        const currentLang = document.getElementById('currentLang');
        if (currentLang) {
            currentLang.textContent = this.t('lang.current');
            console.log('🌐 语言按钮文字已更新:', this.t('lang.current'));
        }
    }
    
    updateRotatingText() {
        const rotatingTexts = document.querySelectorAll('.rotating-text span');
        if (rotatingTexts.length >= 2) {
            rotatingTexts[0].textContent = `"${this.t('home.rotating.text1')}"`;
            rotatingTexts[1].textContent = `"${this.t('home.rotating.text2')}"`;
            console.log('🔄 轮换文本已更新');
        }
    }
    
    updateBrandImage() {
        const brandImage = document.getElementById('brandImage');
        if (brandImage) {
            if (this.currentLanguage === 'zh') {
                brandImage.src = 'images/GABA CHINESE.png';
                brandImage.alt = 'Gaba Chinese';
            } else {
                brandImage.src = 'images/GABA PLANET.png';
                brandImage.alt = 'Gaba Planet';
            }
            console.log('🖼️ 品牌图片已更新为:', brandImage.src);
        }
    }
    
    updateContactEmail() {
        const contactLinks = document.querySelectorAll('a[href^="mailto:lingjingmcn@gmail.com"]');
        if (contactLinks.length > 0) {
            contactLinks.forEach(link => {
                if (this.currentLanguage === 'zh') {
                    link.href = 'mailto:lingjingmcn@gmail.com?subject=与嘎巴星球合作咨询&body=您好！我对嘎巴星球很感兴趣，希望了解更多信息。';
                } else {
                    link.href = 'mailto:lingjingmcn@gmail.com?subject=Partnership Inquiry with Gaba Planet&body=Hello! I am very interested in Gaba Planet and would like to learn more.';
                }
            });
            console.log('📧 邮件链接已更新为:', this.currentLanguage);
        }
    }
    
    t(key) {
        if (!key) return '';
        
        const translation = window.i18nData[this.currentLanguage]?.[key];
        if (!translation) {
            console.warn(`⚠️ 翻译未找到: "${key}"`);
            return key;
        }
        return translation;
    }
    
    getCurrentLanguage() {
        return this.currentLanguage;
    }
}

